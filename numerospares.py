# Realizar un programa que lea un numero entero y nos diga si es par o impar
# operacion módulo, en python el operador es %
# ejemplos 8%3 = 2, 8%4 = 0, 
# datos de entrada: numero
# datos de salida: mensaje ue diga si el numero es par o impar (str


numero = int(input("Digite un número entero\n"))
if numero%2 == 0:
    print(f"El número {numero} es par")
else:
    print(f"El número {numero} es impar")

